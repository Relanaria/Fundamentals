function laguage(lang) {
  if (lang === "England" || lang === "USA") {
    console.log("English");
  } else if (lang === "Spain" || lang === "Argentina" || lang === "Mexico") {
    console.log("Spanish");
  } else {
    console.log("unknown");
  }
}


laguage("Spain");
laguage("USA");
laguage("England");
laguage("Bulgaria");
laguage("Mexico");
laguage("Italy");
